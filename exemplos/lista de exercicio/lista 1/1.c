/*
 Faça um programa para ler três números inteiros e escrever a média dos números lidos
 */

int main(){
    //declarar as variaveis!!!
    int n1,n2,n3;
    float media;
    //entrada
    scanf("%d %d %d",&n1,&n2,&n3);
    //processar
    media=(n1+n2+n3)/3;
    //saida
    printf("a media e igual %f",media);
    return 0;
}
